<template>
  <v-dialog
    :value="loadSvg"
    class="b-loading-svg"
    width="600"
    @click:outside="cancelModal"
    @input="$emit('updateModalValue', $event)"
  >
    <div class="b-loading-svg__content">
      <div class="b-loading-svg__title">
        Загрузка файла
      </div>
      <div class="b-loading-svg__radio">
        <v-radio-group
          v-model="uploadType"
          :color="theme.$primary_button"
        >
          <v-radio
            v-for="option in uploadTypes"
            :key="option.value"
            :label="option.label"
            :value="option.value"
          />
        </v-radio-group>
      </div>
      <div class="b-loading-svg__input">
        <v-file-input
          v-model="file"
          :color="theme.$main_text"
          :style="{ color: theme.$main_text }"
          :rules="[
            value => !value || value.size < 1000000 || 'Размер должен быть меньше 1 МБ!',
            value => !value || value.type === 'image/svg+xml' || 'Недопустимый формат!',
          ]"
          accept="image/svg+xml"
          :label="fileLabel"
          @focus="focus()"
        />
      </div>
      <div
        v-if="message"
        :style="`color: ${color}`"
        class="b-loading-svg__message"
      >
        {{ message }}
      </div>
      <div class="b-loading-svg__row">
        <v-btn
          small
          :color="theme.$primary_button"
          class="b-loading-svg__button"
          :disabled="!file || file.type !== 'image/svg+xml' || file.size > 1000000 || disabled"
          @click="loadFile"
        >
          Отправить
        </v-btn>
        <v-btn
          small
          :color="theme.$primary_button"
          class="b-loading-svg__button"
          @click="cancelModal"
        >
          Закрыть
        </v-btn>
      </div>
    </div>
    <modal-confirm
      v-model="modalValue"
      :theme="theme"
      :modal-text="`Есть несохраненные данные, сохранить?`"
      btn-confirm-text="Да"
      btn-cancel-text="Нет"
      @result="confirm($event)"
    />
  </v-dialog>
</template>

<script>
export default {
  name: 'ModalLoadingSvg',
  model: {
    prop: 'loadSvg',
    event: 'updateLoadingSvg',
  },
  props: {
    loadSvg: {
      type: Boolean,
      default: false,
    },
    uploadTypes: {
      type: Array,
      default: () => ([
        { label: 'SVG', value: 'svg' },
        { label: 'Файл', value: 'file' },
      ]),
    },
  },
  data() {
    return {
      uploadType: 'svg',
      file: null,
      message: '',
      color: '',
      modalValue: false,
      disabled: false,
    };
  },
  computed: {
    theme() {
      return this.$store.getters.getTheme;
    },
    fileLabel() {
      const { uploadTypes, uploadType } = this;
      return `Выберите ${uploadTypes.find(({ value }) => value === uploadType).label.toLowerCase()}`;
    },
  },
  methods: {
    cancelModal() {
      if (this.file) {
        this.modalValue = true;
      } else {
        this.$emit('updateModalValue', false);
      }
    },
    async loadFile() {
      this.disabled = true;
      const formData = new FormData();
      formData.append('file', this.file);
      const res = await this.$store.dispatch('setLoadingSvg', {
        file: this.file,
        formData,
        path: this.uploadType,
      });
      this.color = '';
      this.message = '';
      if (res.status === 'ok' && res?.notifications) {
        this.$store.commit('notify/addNotifications', res.notifications);
        setTimeout(() => {
          this.file = null;
          this.$emit('updateModalValue', false);
        }, 1000);
      } else {
        this.color = 'red';
        this.message = res.statusText ? `Ошибка: ${res.statusText}` : 'Загрузить файл не удалось.';
      }
      this.disabled = false;
    },
    focus() {
      this.color = '';
      this.message = '';
    },
    confirm(val) {
      if (val) {
        this.loadFile();
      } else {
        this.file = null;
        this.$emit('updateModalValue', false);
      }
    },
  },
};
</script>

